// Look up a key for a language; fall back to English, then to `fallback`
// (e.g. the raw data value for unknown topics/reasons), then to the key.
export function dt(lang, key, fallback) {
  const table = DASH[lang] || DASH.en;
  const v = table[key] != null ? table[key] : DASH.en[key];
  if (v != null) return v;
  return fallback != null ? fallback : key;
}
